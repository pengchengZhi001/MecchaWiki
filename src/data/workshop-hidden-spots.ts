import type { SpotCategoryId } from "@/lib/site";
import type { HiddenSpot } from "./hidden-spots";
import { spotDescriptions } from "./spot-descriptions";
import { getSpotImageUrl } from "./spot-images";

type WorkshopSpotInput = {
  slug: string;
  name: string;
  map: string;
  mapSlug: string;
  category: SpotCategoryId;
  survivalRate: number;
  votes: number;
  upvotes: number;
  author: string;
  submittedAt: string;
  tips: string;
  source: string;
  sourceUrl: string;
  featured?: boolean;
  trending?: boolean;
};

function workshopSpot(data: WorkshopSpotInput): HiddenSpot {
  return {
    ...data,
    description: spotDescriptions[data.slug] ?? "",
    views: data.votes * 2 + data.upvotes,
    imageUrl: getSpotImageUrl(data.slug),
  };
}

